"""Micro-theory library: composable logic modules for the reasoning pipeline.

Each micro-theory provides:
- Pre-validated Prolog rules (never generated by the LLM)
- Slot definitions (what the LLM must fill)
- A builder function that assembles facts + rules + query from slots

Theories can be composed: the orchestrator merges multiple theories into
a single connected Prolog program before sending to DALI2.
"""

from __future__ import annotations


# ---------------------------------------------------------------------------
# Theory 1: Transitive Closure
# ---------------------------------------------------------------------------

def build_transitive_closure(slots: dict) -> tuple[dict | None, str]:
    """Transitive closure over a binary relation.

    Slots:
        relation  — name of the base relation (e.g. "parent")
        pairs     — list of [A, B] pairs for the base relation
        derived   — name of the transitive closure predicate (e.g. "ancestor")
        query     — {"entity": X, "target": Y} or {"entity": X, "target": "X"}
    """
    relation = slots.get("relation", "")
    pairs = slots.get("pairs", [])
    derived = slots.get("derived", "")
    query_info = slots.get("query", {})

    if not relation:
        return None, "Slot 'relation' is required (e.g. 'parent')."
    if not pairs:
        return None, "Slot 'pairs' is required (list of [A, B] pairs)."
    if not derived:
        derived = f"{relation}_closure"

    facts = []
    for pair in pairs:
        if isinstance(pair, list) and len(pair) == 2:
            facts.append({"pred": relation, "args": pair})
        elif isinstance(pair, dict):
            facts.append({"pred": relation, "args": [pair.get("from", ""), pair.get("to", "")]})

    # Transitive closure rules:
    # derived(X, Y) :- relation(X, Y).
    # derived(X, Z) :- relation(X, Y), derived(Y, Z).
    rules = [
        {
            "head": {"pred": derived, "args": ["X", "Y"]},
            "body": [{"pred": relation, "args": ["X", "Y"]}],
        },
        {
            "head": {"pred": derived, "args": ["X", "Z"]},
            "body": [
                {"pred": relation, "args": ["X", "Y"]},
                {"pred": derived, "args": ["Y", "Z"]},
            ],
        },
    ]

    # Build query
    if isinstance(query_info, dict):
        entity = query_info.get("entity", "X")
        target = query_info.get("target", "Y")
        query = {"pred": derived, "args": [entity, target]}
    elif isinstance(query_info, str):
        query = {"pred": derived, "args": [query_info, "X"]}
    else:
        query = {"pred": derived, "args": ["X", "Y"]}

    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 2: Disjunctive Exclusion
# ---------------------------------------------------------------------------

def build_disjunctive_exclusion(slots: dict) -> tuple[dict | None, str]:
    """Disjunctive reasoning: exactly one of N alternatives is true.

    Slots:
        alternatives — list of atoms that form the disjunction
        excluded     — list of atoms known to be false
        query        — atom to determine truth value of
    """
    alternatives = slots.get("alternatives", [])
    excluded = slots.get("excluded", [])
    query_atom = slots.get("query", "")

    if len(alternatives) < 2:
        return None, "Slot 'alternatives' needs at least 2 items."
    if not query_atom:
        return None, "Slot 'query' is required."

    facts = []
    for alt in alternatives:
        facts.append({"pred": "alternative", "args": [alt]})
    for exc in excluded:
        facts.append({"pred": "excluded", "args": [exc]})

    # Rules:
    # holds(X) :- alternative(X), \+ excluded(X), 
    #             forall((alternative(Y), Y \= X), excluded(Y)).
    # Simplified: if only one non-excluded alternative remains, it holds.
    rules = [
        {
            "head": {"pred": "holds", "args": ["X"]},
            "body": [
                {"pred": "alternative", "args": ["X"]},
                {"not": {"pred": "excluded", "args": ["X"]}},
                {
                    "forall": {
                        "and": [
                            {"pred": "alternative", "args": ["Y"]},
                            {"pred": "\\=", "args": ["Y", "X"]},
                        ]
                    },
                    "action": {"pred": "excluded", "args": ["Y"]},
                },
            ],
        },
    ]

    query = {"pred": "holds", "args": [query_atom]}
    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 3: Arithmetic Comparison
# ---------------------------------------------------------------------------

def build_arithmetic_compare(slots: dict) -> tuple[dict | None, str]:
    """Arithmetic comparison: find max/min among items with numeric values.

    Slots:
        values    — list of {"entity": name, "value": number}
        criterion — "max" or "min"
        query     — "best" (finds the entity matching the criterion)
    """
    values = slots.get("values", [])
    criterion = slots.get("criterion", "max")

    if not values:
        return None, "Slot 'values' is required (list of {entity, value})."
    if criterion not in ("max", "min"):
        return None, "Slot 'criterion' must be 'max' or 'min'."

    facts = []
    for v in values:
        if isinstance(v, dict):
            facts.append({"pred": "value_of", "args": [v["entity"], v["value"]]})
        elif isinstance(v, list) and len(v) == 2:
            facts.append({"pred": "value_of", "args": v})

    # Comparison operator
    if criterion == "max":
        op = "=<"  # No other Y has a value greater than X's value
    else:
        op = ">="  # No other Y has a value less than X's value

    # best(X) :- value_of(X, V), \+ (value_of(Y, W), Y \= X, W > V).  [for max]
    # best(X) :- value_of(X, V), \+ (value_of(Y, W), Y \= X, W < V).  [for min]
    inner_op = ">" if criterion == "max" else "<"
    rules = [
        {
            "head": {"pred": "best", "args": ["X"]},
            "body": [
                {"pred": "value_of", "args": ["X", "V"]},
                {
                    "not": {
                        "and": [
                            {"pred": "value_of", "args": ["Y", "W"]},
                            {"pred": "\\=", "args": ["Y", "X"]},
                            {"op": inner_op, "left": "W", "right": "V"},
                        ]
                    }
                },
            ],
        },
    ]

    query = {"pred": "best", "args": ["X"]}
    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 4: Counting / Aggregation
# ---------------------------------------------------------------------------

def build_counting(slots: dict) -> tuple[dict | None, str]:
    """Count items satisfying a condition.

    Slots:
        items      — list of {"entity": name, "properties": [prop1, ...]}
        filter     — property or predicate name to count
        query      — "count" (returns the count)
    """
    items = slots.get("items", [])
    filter_prop = slots.get("filter", "")

    if not items:
        return None, "Slot 'items' is required."
    if not filter_prop:
        return None, "Slot 'filter' is required (property to count by)."

    facts = []
    for item in items:
        if isinstance(item, dict):
            entity = item["entity"]
            facts.append({"pred": "item", "args": [entity]})
            for prop in item.get("properties", []):
                facts.append({"pred": "has_property", "args": [entity, prop]})
        elif isinstance(item, str):
            facts.append({"pred": "item", "args": [item]})

    # count_matching(N) :- findall(X, (item(X), has_property(X, filter)), Bag), length(Bag, N).
    rules = [
        {
            "head": {"pred": "count_matching", "args": ["N"]},
            "body": [
                {
                    "findall": "X",
                    "goal": {
                        "and": [
                            {"pred": "item", "args": ["X"]},
                            {"pred": "has_property", "args": ["X", filter_prop]},
                        ]
                    },
                    "bag": "Bag",
                },
                {"pred": "length", "args": ["Bag", "N"]},
            ],
        },
    ]

    query = {"pred": "count_matching", "args": ["N"]}
    return {"facts": facts, "rules": rules, "query": query}, ""


