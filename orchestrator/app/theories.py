# Theory 5: Temporal Ordering
# ---------------------------------------------------------------------------

def build_temporal_ordering(slots: dict) -> tuple[dict | None, str]:
    """Temporal ordering with transitivity.

    Slots:
        events       — list of event names
        before_pairs — list of [earlier, later] pairs
        query        — {"type": "before"/"after", "event1": X, "event2": Y}
    """
    events = slots.get("events", [])
    before_pairs = slots.get("before_pairs", [])
    query_info = slots.get("query", {})

    if not before_pairs:
        return None, "Slot 'before_pairs' is required (list of [earlier, later])."

    facts = []
    for ev in events:
        facts.append({"pred": "event", "args": [ev]})
    for pair in before_pairs:
        if isinstance(pair, list) and len(pair) == 2:
            facts.append({"pred": "directly_before", "args": pair})
        elif isinstance(pair, dict):
            facts.append({"pred": "directly_before", "args": [pair["earlier"], pair["later"]]})

    # Transitive before:
    # before(X, Y) :- directly_before(X, Y).
    # before(X, Z) :- directly_before(X, Y), before(Y, Z).
    # after(X, Y) :- before(Y, X).
    rules = [
        {
            "head": {"pred": "before", "args": ["X", "Y"]},
            "body": [{"pred": "directly_before", "args": ["X", "Y"]}],
        },
        {
            "head": {"pred": "before", "args": ["X", "Z"]},
            "body": [
                {"pred": "directly_before", "args": ["X", "Y"]},
                {"pred": "before", "args": ["Y", "Z"]},
            ],
        },
        {
            "head": {"pred": "after", "args": ["X", "Y"]},
            "body": [{"pred": "before", "args": ["Y", "X"]}],
        },
    ]

    # Build query
    if isinstance(query_info, dict):
        qtype = query_info.get("type", "before")
        e1 = query_info.get("event1", "X")
        e2 = query_info.get("event2", "Y")
        query = {"pred": qtype, "args": [e1, e2]}
    else:
        query = {"pred": "before", "args": ["X", "Y"]}

    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 6: Modus Tollens
# ---------------------------------------------------------------------------

def build_modus_tollens(slots: dict) -> tuple[dict | None, str]:
    """Modus tollens: if A→B and ¬B, then ¬A.

    Slots:
        implications      — list of {"if": A, "then": B}
        negated_conclusions — list of atoms known to be FALSE
        query             — atom to determine (will check if it's negated)
    """
    implications = slots.get("implications", [])
    negated_conclusions = slots.get("negated_conclusions", [])
    query_atom = slots.get("query", "")

    if not implications:
        return None, "Slot 'implications' is required."
    if not negated_conclusions:
        return None, "Slot 'negated_conclusions' is required (atoms known false)."
    if not query_atom:
        return None, "Slot 'query' is required."

    facts = []
    for neg in negated_conclusions:
        facts.append({"pred": "known_false", "args": [neg]})

    # For each implication A→B, add: implies(a, b).
    for impl in implications:
        if isinstance(impl, dict):
            facts.append({"pred": "implies", "args": [impl["if"], impl["then"]]})
        elif isinstance(impl, list) and len(impl) == 2:
            facts.append({"pred": "implies", "args": impl})

    # Modus tollens: if A→B and B is false, then A is false
    # negated(A) :- implies(A, B), known_false(B).
    # negated(A) :- implies(A, B), negated(B).  (chain)
    rules = [
        {
            "head": {"pred": "negated", "args": ["A"]},
            "body": [
                {"pred": "implies", "args": ["A", "B"]},
                {"pred": "known_false", "args": ["B"]},
            ],
        },
        {
            "head": {"pred": "negated", "args": ["A"]},
            "body": [
                {"pred": "implies", "args": ["A", "B"]},
                {"pred": "negated", "args": ["B"]},
            ],
        },
    ]

    query = {"pred": "negated", "args": [query_atom]}
    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 7: Set Membership & Quantification
# ---------------------------------------------------------------------------

def build_set_membership(slots: dict) -> tuple[dict | None, str]:
    """Set-based reasoning with quantifiers (all/some/none).

    Slots:
        sets       — dict of {"set_name": [members...]}
        properties — list of {"entity": name, "property": prop}
        quantifier — "all", "some", "none"
        set_name   — which set to quantify over
        property   — which property to check
        query      — description of what to verify
    """
    sets = slots.get("sets", {})
    properties = slots.get("properties", [])
    quantifier = slots.get("quantifier", "all")
    set_name = slots.get("set_name", "")
    prop = slots.get("property", "")

    if not sets:
        return None, "Slot 'sets' is required."
    if not set_name or set_name not in sets:
        return None, f"Slot 'set_name' must be one of: {list(sets.keys())}."
    if not prop:
        return None, "Slot 'property' is required."

    facts = []
    for sname, members in sets.items():
        for m in members:
            facts.append({"pred": "member_of", "args": [m, sname]})
    for p in properties:
        if isinstance(p, dict):
            facts.append({"pred": "has_prop", "args": [p["entity"], p["property"]]})

    if quantifier == "all":
        # all_have(Set, Prop) :- \+ (member_of(X, Set), \+ has_prop(X, Prop)).
        rules = [
            {
                "head": {"pred": "all_have", "args": [set_name, prop]},
                "body": [
                    {
                        "not": {
                            "and": [
                                {"pred": "member_of", "args": ["X", set_name]},
                                {"not": {"pred": "has_prop", "args": ["X", prop]}},
                            ]
                        }
                    }
                ],
            },
        ]
        query = {"pred": "all_have", "args": [set_name, prop]}
    elif quantifier == "some":
        # some_have(Set, Prop) :- member_of(X, Set), has_prop(X, Prop).
        rules = [
            {
                "head": {"pred": "some_have", "args": [set_name, prop]},
                "body": [
                    {"pred": "member_of", "args": ["X", set_name]},
                    {"pred": "has_prop", "args": ["X", prop]},
                ],
            },
        ]
        query = {"pred": "some_have", "args": [set_name, prop]}
    elif quantifier == "none":
        # none_have(Set, Prop) :- \+ (member_of(X, Set), has_prop(X, Prop)).
        rules = [
            {
                "head": {"pred": "none_have", "args": [set_name, prop]},
                "body": [
                    {
                        "not": {
                            "and": [
                                {"pred": "member_of", "args": ["X", set_name]},
                                {"pred": "has_prop", "args": ["X", prop]},
                            ]
                        }
                    }
                ],
            },
        ]
        query = {"pred": "none_have", "args": [set_name, prop]}
    else:
        return None, f"Quantifier must be 'all', 'some', or 'none', got '{quantifier}'."

    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 8: Constraint Assignment (Puzzle-like)
# ---------------------------------------------------------------------------

def build_constraint_assignment(slots: dict) -> tuple[dict | None, str]:
    """Constraint satisfaction: assign properties to entities obeying constraints.

    Slots:
        entities    — list of entity names
        properties  — list of property values to assign
        constraints — list of constraints:
            {"type": "assign", "entity": E, "property": P}  (E must have P)
            {"type": "exclude", "entity": E, "property": P} (E must NOT have P)
            {"type": "same", "entity1": E1, "entity2": E2, "property": P} (share property)
            {"type": "different", "entity1": E1, "entity2": E2} (different properties)
        query       — {"entity": E} to find its assignment
    """
    entities = slots.get("entities", [])
    properties = slots.get("properties", [])
    constraints = slots.get("constraints", [])
    query_info = slots.get("query", {})

    if not entities:
        return None, "Slot 'entities' is required."
    if not properties:
        return None, "Slot 'properties' is required."

    facts = []
    for e in entities:
        facts.append({"pred": "entity", "args": [e]})
    for p in properties:
        facts.append({"pred": "property", "args": [p]})

    # Process constraints into facts
    for c in constraints:
        ctype = c.get("type", "")
        if ctype == "assign":
            facts.append({"pred": "must_have", "args": [c["entity"], c["property"]]})
        elif ctype == "exclude":
            facts.append({"pred": "cannot_have", "args": [c["entity"], c["property"]]})
        elif ctype == "different":
            facts.append({"pred": "different_from", "args": [c["entity1"], c["entity2"]]})

    # Rules for constraint solving:
    # assigned(E, P) :- must_have(E, P).
    # assigned(E, P) :- entity(E), property(P), \+ cannot_have(E, P),
    #                   \+ (must_have(E, Q), Q \= P),
    #                   \+ (must_have(F, P), F \= E).
    rules = [
        {
            "head": {"pred": "assigned", "args": ["E", "P"]},
            "body": [{"pred": "must_have", "args": ["E", "P"]}],
        },
        {
            "head": {"pred": "assigned", "args": ["E", "P"]},
            "body": [
                {"pred": "entity", "args": ["E"]},
                {"pred": "property", "args": ["P"]},
                {"not": {"pred": "cannot_have", "args": ["E", "P"]}},
                {"not": {
                    "and": [
                        {"pred": "must_have", "args": ["E", "Q"]},
                        {"pred": "\\=", "args": ["Q", "P"]},
                    ]
                }},
                {"not": {
                    "and": [
                        {"pred": "must_have", "args": ["F", "P"]},
                        {"pred": "\\=", "args": ["F", "E"]},
                    ]
                }},
            ],
        },
    ]

    # Build query
    if isinstance(query_info, dict) and "entity" in query_info:
        query = {"pred": "assigned", "args": [query_info["entity"], "X"]}
    else:
        query = {"pred": "assigned", "args": ["X", "Y"]}

    return {"facts": facts, "rules": rules, "query": query}, ""


