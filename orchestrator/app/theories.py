# Theory 9: Causal Chain
# ---------------------------------------------------------------------------

def build_causal_chain(slots: dict) -> tuple[dict | None, str]:
    """Causal reasoning: causes propagate through chains.

    Slots:
        initial_causes — list of atoms that are initially true/caused
        cause_rules    — list of {"cause": A, "effect": B}
        query          — atom to check if it's caused
    """
    initial_causes = slots.get("initial_causes", [])
    cause_rules = slots.get("cause_rules", [])
    query_atom = slots.get("query", "")

    if not initial_causes:
        return None, "Slot 'initial_causes' is required."
    if not cause_rules:
        return None, "Slot 'cause_rules' is required."
    if not query_atom:
        return None, "Slot 'query' is required."

    facts = []
    for cause in initial_causes:
        facts.append({"pred": "active", "args": [cause]})
    for cr in cause_rules:
        if isinstance(cr, dict):
            facts.append({"pred": "causes", "args": [cr["cause"], cr["effect"]]})
        elif isinstance(cr, list) and len(cr) == 2:
            facts.append({"pred": "causes", "args": cr})

    # caused(X) :- active(X).
    # caused(X) :- causes(Y, X), caused(Y).
    rules = [
        {
            "head": {"pred": "caused", "args": ["X"]},
            "body": [{"pred": "active", "args": ["X"]}],
        },
        {
            "head": {"pred": "caused", "args": ["X"]},
            "body": [
                {"pred": "causes", "args": ["Y", "X"]},
                {"pred": "caused", "args": ["Y"]},
            ],
        },
    ]

    query = {"pred": "caused", "args": [query_atom]}
    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 10: Default with Exceptions
# ---------------------------------------------------------------------------

def build_default_exception(slots: dict) -> tuple[dict | None, str]:
    """Default reasoning with exceptions (non-monotonic).

    Slots:
        defaults    — list of {"class": C, "property": P} (members of C normally have P)
        exceptions  — list of {"class": C, "property": P} (members of C do NOT have P)
        instances   — list of {"entity": E, "class": C}
        query       — {"entity": E, "property": P} to check
    """
    defaults = slots.get("defaults", [])
    exceptions = slots.get("exceptions", [])
    instances = slots.get("instances", [])
    query_info = slots.get("query", {})

    if not defaults:
        return None, "Slot 'defaults' is required."
    if not instances:
        return None, "Slot 'instances' is required."

    facts = []
    for inst in instances:
        if isinstance(inst, dict):
            facts.append({"pred": "instance_of", "args": [inst["entity"], inst["class"]]})
        elif isinstance(inst, list) and len(inst) == 2:
            facts.append({"pred": "instance_of", "args": inst})

    for d in defaults:
        facts.append({"pred": "default_prop", "args": [d["class"], d["property"]]})
    for e in exceptions:
        facts.append({"pred": "exception", "args": [e["class"], e["property"]]})

    # has(E, P) :- instance_of(E, C), default_prop(C, P), \+ exception_applies(E, P).
    # exception_applies(E, P) :- instance_of(E, C), exception(C, P).
    rules = [
        {
            "head": {"pred": "has", "args": ["E", "P"]},
            "body": [
                {"pred": "instance_of", "args": ["E", "C"]},
                {"pred": "default_prop", "args": ["C", "P"]},
                {"not": {"pred": "exception_applies", "args": ["E", "P"]}},
            ],
        },
        {
            "head": {"pred": "exception_applies", "args": ["E", "P"]},
            "body": [
                {"pred": "instance_of", "args": ["E", "C"]},
                {"pred": "exception", "args": ["C", "P"]},
            ],
        },
    ]

    # Build query
    if isinstance(query_info, dict):
        entity = query_info.get("entity", "X")
        prop = query_info.get("property", "P")
        query = {"pred": "has", "args": [entity, prop]}
    else:
        query = {"pred": "has", "args": ["X", "P"]}

    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 11: Spatial Reasoning
# ---------------------------------------------------------------------------

def build_spatial_reasoning(slots: dict) -> tuple[dict | None, str]:
    """Spatial reasoning with transitivity.

    Slots:
        relations  — list of {"type": "left_of"/"right_of"/"above"/"below",
                               "entity1": A, "entity2": B}
        query      — {"type": relation_type, "entity1": A, "entity2": B}
    """
    relations = slots.get("relations", [])
    query_info = slots.get("query", {})

    if not relations:
        return None, "Slot 'relations' is required."

    facts = []
    for rel in relations:
        rtype = rel.get("type", "left_of")
        facts.append({"pred": rtype, "args": [rel["entity1"], rel["entity2"]]})

    # Transitivity + inverses for spatial relations
    rules = [
        # left_of transitive
        {"head": {"pred": "left_of", "args": ["X", "Z"]},
         "body": [{"pred": "left_of", "args": ["X", "Y"]}, {"pred": "left_of", "args": ["Y", "Z"]}]},
        # right_of is inverse of left_of
        {"head": {"pred": "right_of", "args": ["X", "Y"]},
         "body": [{"pred": "left_of", "args": ["Y", "X"]}]},
        # above transitive
        {"head": {"pred": "above", "args": ["X", "Z"]},
         "body": [{"pred": "above", "args": ["X", "Y"]}, {"pred": "above", "args": ["Y", "Z"]}]},
        # below is inverse of above
        {"head": {"pred": "below", "args": ["X", "Y"]},
         "body": [{"pred": "above", "args": ["Y", "X"]}]},
        # between: X is between A and B if A left_of X and X left_of B (or reverse)
        {"head": {"pred": "between", "args": ["X", "A", "B"]},
         "body": [{"pred": "left_of", "args": ["A", "X"]}, {"pred": "left_of", "args": ["X", "B"]}]},
    ]

    # Build query
    if isinstance(query_info, dict):
        qtype = query_info.get("type", "left_of")
        e1 = query_info.get("entity1", "X")
        e2 = query_info.get("entity2", "Y")
        query = {"pred": qtype, "args": [e1, e2]}
    else:
        query = {"pred": "left_of", "args": ["X", "Y"]}

    return {"facts": facts, "rules": rules, "query": query}, ""


# ---------------------------------------------------------------------------
# Theory 12: Planning (Simple STRIPS-like)
# ---------------------------------------------------------------------------

def build_planning(slots: dict) -> tuple[dict | None, str]:
    """Simple planning: find which action achieves a goal from initial state.

    Slots:
        initial_state — list of atoms true in the initial state
        actions       — list of {"name": N, "preconditions": [...], "effects": [...]}
        goal          — list of atoms that must be true in the goal state
    """
    initial_state = slots.get("initial_state", [])
    actions = slots.get("actions", [])
    goal = slots.get("goal", [])

    if not actions:
        return None, "Slot 'actions' is required."
    if not goal:
        return None, "Slot 'goal' is required."

    facts = []
    for state_atom in initial_state:
        facts.append({"pred": "holds_in_initial", "args": [state_atom]})

    for action in actions:
        name = action["name"]
        facts.append({"pred": "action", "args": [name]})
        for pre in action.get("preconditions", []):
            facts.append({"pred": "precond", "args": [name, pre]})
        for eff in action.get("effects", []):
            facts.append({"pred": "adds", "args": [name, eff]})
        for neg in action.get("deletes", []):
            facts.append({"pred": "deletes", "args": [name, neg]})

    for g in goal:
        facts.append({"pred": "goal_cond", "args": [g]})

    # applicable(A) :- action(A), \+ (precond(A, P), \+ holds_in_initial(P)).
    # achieves_goal(A) :- applicable(A), \+ (goal_cond(G), \+ after_action(A, G)).
    # after_action(A, G) :- adds(A, G).
    # after_action(A, G) :- holds_in_initial(G), \+ deletes(A, G).
    rules = [
        {
            "head": {"pred": "applicable", "args": ["A"]},
            "body": [
                {"pred": "action", "args": ["A"]},
                {"not": {
                    "and": [
                        {"pred": "precond", "args": ["A", "P"]},
                        {"not": {"pred": "holds_in_initial", "args": ["P"]}},
                    ]
                }},
            ],
        },
        {
            "head": {"pred": "after_action", "args": ["A", "G"]},
            "body": [{"pred": "adds", "args": ["A", "G"]}],
        },
        {
            "head": {"pred": "after_action", "args": ["A", "G"]},
            "body": [
                {"pred": "holds_in_initial", "args": ["G"]},
                {"not": {"pred": "deletes", "args": ["A", "G"]}},
            ],
        },
        {
            "head": {"pred": "achieves_goal", "args": ["A"]},
            "body": [
                {"pred": "applicable", "args": ["A"]},
                {"not": {
                    "and": [
                        {"pred": "goal_cond", "args": ["G"]},
                        {"not": {"pred": "after_action", "args": ["A", "G"]}},
                    ]
                }},
            ],
        },
    ]

    query = {"pred": "achieves_goal", "args": ["X"]}
    return {"facts": facts, "rules": rules, "query": query}, ""


